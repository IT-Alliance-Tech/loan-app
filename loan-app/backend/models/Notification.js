const mongoose = require("mongoose");

const NotificationSchema = new mongoose.Schema({
  recipient: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  sender: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
  },
  type: {
    type: String,
    enum: ["PAYMENT_REQUEST", "PAYMENT_APPROVED", "PAYMENT_REJECTED", "SYSTEM"],
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  data: {
    loanNumber: String,
    customerName: String,
    amount: Number,
    requestTime: {
      type: Date,
      default: Date.now
    },
    employeeName: String,
    loanId: String,
    loanType: String,
    approvalId: String,
  },
  isRead: {
    type: Boolean,
    default: false,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Notification", NotificationSchema);
